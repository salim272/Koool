// Helper function to get unique brands from the data
export const getBrands = (): Brand[] => {
  const uniqueBrands = [...new Set(vehicles.map((vehicle) => vehicle.make))]
  return uniqueBrands.map((brand) => {
    const vehicle = vehicles.find((v) => v.make === brand)
    if (!vehicle) {
      throw new Error(`Vehicle not found for brand: ${brand}`)
    }
    return {
      id: vehicle.vehicleId,
      name: brand,
      logo: `${vehicle.mediaDomainUri}${vehicle.mediaFolderPath}${vehicle.makeImageFileName}`,
    }
  })
}

// Helper function to get models by brand
export const getModelsByBrand = (brand: string): Model[] => {
  return vehicles
    .filter((vehicle) => vehicle.make === brand)
    .map((vehicle) => ({
      id: vehicle.vehicleId,
      name: vehicle.name,
      variant: vehicle.modelVariant,
      // Since there's no model image, we'll use a placeholder
      image: "/placeholder.svg?height=200&width=200",
      fuelType: vehicle.fuelType,
      releaseDate: vehicle.releaseDate,
      expiryDate: vehicle.expiryDate,
    }))
}

// Helper function to get all unique fuel types
export const getFuelTypes = (): string[] => {
  return [...new Set(vehicles.map((vehicle) => vehicle.fuelType))]
}

// Helper function to get all unique years
export const getYears = (): string[] => {
  const years = new Set<string>()
  vehicles.forEach((vehicle) => {
    if (vehicle.releaseDate && vehicle.releaseDate !== "NOW") {
      years.add(vehicle.releaseDate)
    }
    if (vehicle.expiryDate && vehicle.expiryDate !== "NOW") {
      years.add(vehicle.expiryDate)
    }
  })
  return [...years].sort()
}

export default vehicles
